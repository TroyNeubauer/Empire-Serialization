package com.troy.serialization.exception;

public class TroySerializationIOException extends RuntimeException {

	public TroySerializationIOException() {
		super();
	}

	public TroySerializationIOException(String message, Throwable cause) {
		super(message, cause);
	}

	public TroySerializationIOException(String message) {
		super(message);
	}

	public TroySerializationIOException(Throwable cause) {
		super(cause);
	}

}
